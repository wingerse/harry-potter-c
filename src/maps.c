void init_tile_axes()
{
    for (size_t i = 0; i < TILECOUNT; i++) {
        Vec* vertices = tile_vertices[i];
        Vec* axes = tile_axes[i];
        for (size_t j = 0; j < tile_vertices_lens[i]; j++) {
            Vec edge = Vec_sub(vertices[j], vertices[j+1 == tile_vertices_lens[i] ? 0 : j+1]);
            Vec normal = {-edge.y, edge.x};
            axes[j] = normal;
        }
    }
}